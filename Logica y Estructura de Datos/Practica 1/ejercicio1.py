#Dados dos números, calcular su:
#suma
#producto
#diferencia
#relación (división)
#E informar los resultados.

#leer
numero1 = int(input("ingrese el número que desee: "))
numero2 = int(input("ingrese otro más: "))

#asignar
suma = numero1 + numero2
producto = numero1 * numero2
diferencia = numero1 - numero2
división = numero1 / numero2

#escribir
print(f"la suma es: {suma}, su producto es: {producto}, su resta da: {diferencia}, y la división es: {división}")
